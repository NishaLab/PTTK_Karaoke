package Model;

import java.io.Serializable;

/**
 *
 * @author LEGION
 */
public class BookedItem implements Serializable{
    private Item item;
    private int quantity;
    private float price;

    public BookedItem() {
    }

    public BookedItem(Item item, int quantity, float price) {
        this.item = item;
        this.quantity = quantity;
        this.price = price;
    }

    public Item getItem() {
        return item;
    }

    public void setItem(Item item) {
        this.item = item;
    }

    public int getQuantity() {
        return quantity;
    }

    public void setQuantity(int quantity) {
        this.quantity = quantity;
    }

    public float getPrice() {
        return price;
    }

    public void setPrice(float price) {
        this.price = price;
    }

    @Override
    public String toString() {
        return "BookedItem{" + "item=" + item + ", quantity=" + quantity + ", price=" + price + '}';
    }
    
}
